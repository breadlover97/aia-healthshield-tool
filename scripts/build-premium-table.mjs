import { readFile, writeFile } from "node:fs/promises";

const residentPath = new URL("../public/data/resident-premiums.csv", import.meta.url);
const foreignerPath = new URL("../public/data/foreigner-premiums.csv", import.meta.url);
const outputPath = new URL("../public/data/premium-table.js", import.meta.url);

const residentColumns = [
  "age_band",
  "min_age",
  "max_age",
  "msl",
  "awl",
  "hsg_max_a_base",
  "hsg_max_a_cash",
  "hsg_max_b_base",
  "hsg_max_b_cash",
  "hsg_max_b_lite_base",
  "hsg_max_b_lite_cash",
  "vhp_a_cash",
  "vhp_b_cash",
  "vhp_b_lite_cash",
];

const foreignerColumns = [
  "age_band",
  "min_age",
  "max_age",
  "awl",
  "hsg_max_a_dependent_base",
  "hsg_max_a_dependent_cash",
  "hsg_max_a_non_dependent_cash",
  "hsg_max_b_dependent_base",
  "hsg_max_b_dependent_cash",
  "hsg_max_b_non_dependent_cash",
  "vhp_a_dependent_cash",
  "vhp_a_non_dependent_cash",
  "vhp_b_dependent_cash",
  "vhp_b_non_dependent_cash",
];

function parseCsv(text, requiredColumns, sourceName) {
  const lines = text.trim().split(/\r?\n/).filter(Boolean);
  if (lines.length < 2) throw new Error(`${sourceName} must include a header row and at least one data row.`);

  const header = lines[0].split(",").map((cell) => cell.trim());
  const missing = requiredColumns.filter((column) => !header.includes(column));
  if (missing.length) throw new Error(`${sourceName} is missing columns: ${missing.join(", ")}`);

  return lines.slice(1).map((line, lineIndex) => {
    const cells = line.split(",").map((cell) => cell.trim());
    if (cells.length !== header.length) {
      throw new Error(`${sourceName} row ${lineIndex + 2} has ${cells.length} cells; expected ${header.length}.`);
    }
    return Object.fromEntries(header.map((column, index) => [column, cells[index]]));
  });
}

function number(row, key, sourceName) {
  const value = Number(row[key]);
  if (!Number.isFinite(value)) throw new Error(`${sourceName} has an invalid number in ${key} for age band ${row.age_band}.`);
  return value;
}

function residentBand(row) {
  return {
    label: row.age_band,
    min: number(row, "min_age", "resident-premiums.csv"),
    max: number(row, "max_age", "resident-premiums.csv"),
    msl: number(row, "msl", "resident-premiums.csv"),
    awl: number(row, "awl", "resident-premiums.csv"),
    A: [number(row, "hsg_max_a_base", "resident-premiums.csv"), number(row, "hsg_max_a_cash", "resident-premiums.csv")],
    B: [number(row, "hsg_max_b_base", "resident-premiums.csv"), number(row, "hsg_max_b_cash", "resident-premiums.csv")],
    LITE: [number(row, "hsg_max_b_lite_base", "resident-premiums.csv"), number(row, "hsg_max_b_lite_cash", "resident-premiums.csv")],
    vhpA: number(row, "vhp_a_cash", "resident-premiums.csv"),
    vhpB: number(row, "vhp_b_cash", "resident-premiums.csv"),
    vhpLite: number(row, "vhp_b_lite_cash", "resident-premiums.csv"),
  };
}

function foreignerBand(row) {
  return {
    label: row.age_band,
    min: number(row, "min_age", "foreigner-premiums.csv"),
    max: number(row, "max_age", "foreigner-premiums.csv"),
    awl: number(row, "awl", "foreigner-premiums.csv"),
    A: {
      dependent: [number(row, "hsg_max_a_dependent_base", "foreigner-premiums.csv"), number(row, "hsg_max_a_dependent_cash", "foreigner-premiums.csv")],
      nonDependent: number(row, "hsg_max_a_non_dependent_cash", "foreigner-premiums.csv"),
    },
    B: {
      dependent: [number(row, "hsg_max_b_dependent_base", "foreigner-premiums.csv"), number(row, "hsg_max_b_dependent_cash", "foreigner-premiums.csv")],
      nonDependent: number(row, "hsg_max_b_non_dependent_cash", "foreigner-premiums.csv"),
    },
    vhpA: {
      dependent: number(row, "vhp_a_dependent_cash", "foreigner-premiums.csv"),
      nonDependent: number(row, "vhp_a_non_dependent_cash", "foreigner-premiums.csv"),
    },
    vhpB: {
      dependent: number(row, "vhp_b_dependent_cash", "foreigner-premiums.csv"),
      nonDependent: number(row, "vhp_b_non_dependent_cash", "foreigner-premiums.csv"),
    },
  };
}

const residentRows = parseCsv(await readFile(residentPath, "utf8"), residentColumns, "resident-premiums.csv");
const foreignerRows = parseCsv(await readFile(foreignerPath, "utf8"), foreignerColumns, "foreigner-premiums.csv");
const ageBands = residentRows.map(residentBand);
const foreignerBands = foreignerRows.map(foreignerBand);

const moduleText = `// Generated by scripts/build-premium-table.mjs from public/data/*.csv.\n// Edit the CSV files, then run npm run build:premiums.\n\nexport const AGE_BANDS = ${JSON.stringify(ageBands, null, 2)};\n\nexport const FOREIGNER_BANDS = ${JSON.stringify(foreignerBands, null, 2)};\n`;

await writeFile(outputPath, moduleText);
console.log(`Generated ${outputPath.pathname}`);
